package OOP;

public class WrapperClassInJava {
    @SuppressWarnings("unused")
    public static void main(String[] args) {
        int a = 10; //
        Integer b = 50; // Wrapper Class

        // Auto-Boxing
        Integer c = a;

        // Auto-Unboxing
        int d = b;

        // Character Array
        char arr[] = new char[5];

        // Wrapper Class Array
        Character arr1[] = new Character[6];
    }
}
