package Programs;
import java.util.Scanner;

class Input {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int a = sc.nextInt();
		int b = sc.nextInt();
		System.out.println("Sum of " + a + " & " + b + " = " + (a + b));
		sc.close();
	}
}
